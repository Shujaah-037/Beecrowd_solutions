#Input and Output of Integers
a, b, c = map(int, input().split())

print("A = {}, B = {}, C = {}".format(a, b, c))
print("A = {:10}, B = {:10}, C = {:10}".format(a, b, c))
print("A = {:010}, B = {:010}, C = {:010}".format(a, b, c))
print("A = {:-10}, B = {:-10}, C = {:-10}".format(a, b, c))
